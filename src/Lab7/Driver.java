class Driver {
    String name;
    int age;
    int driving_experience;
    public Driver (String name, int age, int driving_experience) {
        this.name = name;
        this.age = age;
        this.driving_experience = driving_experience;
    }
    public  Integer getAge() {
        return age;
    }
    public Integer getDriving_experience() {
        return driving_experience;
    }
    public String getNamea() {
        return name;
    }

}

